CREATE DATABASE IF NOT EXISTS grades;
USE grades

DROP TABLE IF EXISTS grade,
                     student,
                     course,
                     semester;


CREATE TABLE student
  (
    studentID INT UNSIGNED AUTO_INCREMENT PRIMARY KEY,
    studentNumber CHAR(9) UNIQUE NOT NULL,
    studentFname VARCHAR(25),
    studentLname VARCHAR(25),
    studentGender ENUM('M', 'F'),
    studentDOB DATE
  );

CREATE TABLE course
  (
    courseID INT UNSIGNED AUTO_INCREMENT PRIMARY KEY,
    courseCode CHAR(6) UNIQUE NOT NULL,
    courseName VARCHAR(65) NOT NULL
  );

CREATE TABLE semester
  (
    semesterID INT UNSIGNED AUTO_INCREMENT PRIMARY KEY,
    semesterCode CHAR(10) UNIQUE NOT NULL,
    semesterYear INT,
    semesterSeason ENUM('Summer', 'Fall', 'Winter')
  );

CREATE TABLE grade
  (
    studentID INT UNSIGNED,
    courseID INT UNSIGNED,
    semesterID INT UNSIGNED,
    finalGradePercent NUMERIC(5,2) UNSIGNED NOT NULL DEFAULT 0,
    finalGradeLetter CHAR(2) NOT NULL DEFAULT 'F',
    PRIMARY KEY (studentID, courseID, semesterID),
    CONSTRAINT fkStudentID FOREIGN KEY (studentID) REFERENCES student(studentID),
    CONSTRAINT fkCourseID FOREIGN KEY (courseID) REFERENCES course(courseID),
    CONSTRAINT fkSemesterID FOREIGN KEY (semesterID) REFERENCES semester(semesterID)
  );

